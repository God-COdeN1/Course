try:
    # Спроба виконати ділення на нуль
    result = 10 / 0
except ZeroDivisionError:
    # Обробка помилки ділення на нуль
    print("Помилка: Ділення на нуль!")
else:
    # Цей блок не виконується через виключення
    print("Результат:", result)
finally:
    # Завжди виконується, незалежно від винятку
    print("Завершення обробки.")
